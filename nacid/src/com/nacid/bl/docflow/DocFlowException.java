package com.nacid.bl.docflow;

public class DocFlowException extends Exception {

	public DocFlowException() {
	}

	public DocFlowException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DocFlowException(String arg0) {
		super(arg0);
	}

	public DocFlowException(Throwable arg0) {
		super(arg0);
	}

}
